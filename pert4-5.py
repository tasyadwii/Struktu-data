#ARITMATIKA
x = 9
y = 3

#penjumlahan 
hasil = x + y
print(x,'+' , y, '=', hasil)

#pengurangan 
hasil = x - y
print(x,'-' , y, '=', hasil)

#perkalian
hasil = x * y
print(x,'*' , y,'=', hasil)

#pembagian
hasil = x / y
print(x,'/' , y, '=',hasil)

#perpangkatan
hasil = x % y
print(x,'%' , y, '=',hasil)

#modulus
hasil = x ** y 
print(x,'**' , y, '=', hasil)

#floor division
hasil = x // y 
print(x,'//', y, '=', hasil)

#skala prioritas
hasil =(x + y)
