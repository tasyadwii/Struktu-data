# belajar tipe data 

# tipe data integer : bilangan bulat 
bil_integer = 100
print (type(bil_integer))
print ("tipe data dari bil_integer adalah : ",(type (bil_integer)))

# tipe data float : bilangan desimal 
bil_float = 2.5
print ("tipe data dari bil_float adalah : ",(type (bil_float)))
print (type(bil_float))

# tipe data boolean : 0/1, ture/false, ya/tidak 
bil_bol = False
print (type(bil_bol))
print ("tipe data dari bil_bol adalah : ",(type(bil_bol)))

# tipe data stirng : kumpulan karakter 
bil_stirng = "300000.5"
print (type(bil_stirng))
print ("tipe data dari bil_stirng adalah : ",(type(bil_stirng)))

#tipe data dari c
from ctypes import c_double
bil_double = c_double(300000000)
print (type(bil_double))

